create table [Samples].[Movie] (
    [Id] uniqueidentifier not null,
    [VersionOf] uniqueidentifier not null,
    [Updated] datetimeoffset(7) default(sysdatetimeoffset()) not null,
    [UpdatedBy] uniqueidentifier not null,
    [IsDeleted] bit default(0) not null,
    [ExternalId] uniqueidentifier null,
    [Title] nvarchar(160) not null,
    [GenreId] uniqueidentifier not null,
    [RatingId] uniqueidentifier not null,
    [Released] datetimeoffset(7) null,
    [RuntimeMin] int not null,
    [Score] real null,
    [Summary] nvarchar(max) null,
    [PosterImageId] uniqueidentifier null,
    [TrailerVideoId] uniqueidentifier null,
    [Featured] bit default(0) not null,
    constraint [PK_Samples_Movie] primary key clustered ([Id]),
    constraint [FK_Samples_Movie_Genre] foreign key ([GenreId]) references [Samples].[Genre] ([Id]),
    constraint [FK_Samples_Movie_Rating] foreign key ([RatingId]) references [Samples].[Rating] ([Id]),
    constraint [FK_Samples_Movie_PosterImage] foreign key ([PosterImageId]) references [Framework].[ImageFile] ([Id]),
    constraint [FK_Samples_Movie_TrailerVideo] foreign key ([TrailerVideoId]) references [Framework].[VideoFile] ([Id]),
);