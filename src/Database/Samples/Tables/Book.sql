create table [Samples].[Book] (
    [Id] uniqueidentifier not null,
    [VersionOf] uniqueidentifier not null,
    [Updated] datetimeoffset(7) default(sysdatetimeoffset()) not null,
    [UpdatedBy] uniqueidentifier not null,
    [IsDeleted] bit default(0) not null,
    [ExternalId] uniqueidentifier null,
    [Isbn] nvarchar(20) not null,
    [Title] nvarchar(160) not null,
    [Author] nvarchar(120) not null,
    [GenreId] uniqueidentifier not null,
    [Pages] int not null,
    [Price] real not null,
    [Summary] nvarchar(max) null,
    [CoverImageId] uniqueidentifier null,
    [SamplePdfId] uniqueidentifier null,
    [PreviewAudioFileId] uniqueidentifier null,
    [Featured] bit default(0) not null,
    constraint [PK_Samples_Book] primary key clustered ([Id]),
    constraint [FK_Samples_Book_Genre] foreign key ([GenreId]) references [Samples].[Genre] ([Id]),
    constraint [FK_Samples_Book_CoverImage] foreign key ([CoverImageId]) references [Framework].[ImageFile] ([Id]),
    constraint [FK_Samples_Book_SamplePdf] foreign key ([SamplePdfId]) references [Framework].[PdfFile] ([Id]),
    constraint [FK_Samples_Book_PreviewAudioFile] foreign key ([PreviewAudioFileId]) references [Framework].[AudioFile] ([Id]),
);