create table [Samples].[Shirt] (
    [Id] uniqueidentifier not null,
    [VersionOf] uniqueidentifier not null,
    [Updated] datetimeoffset(7) default(sysdatetimeoffset()) not null,
    [UpdatedBy] uniqueidentifier not null,
    [IsDeleted] bit default(0) not null,
    [Name] nvarchar(120) not null,
    [BrandId] uniqueidentifier not null,
    [Fit] int default(0) not null,
    [Material] nvarchar(80) null,
    [Price] real not null,
    [HeroImageId] uniqueidentifier null,
    [GuidePdfId] uniqueidentifier null,
    [Featured] bit default(0) not null,
    constraint [PK_Samples_Shirt] primary key clustered ([Id]),
    constraint [FK_Samples_Shirt_Brand] foreign key ([BrandId]) references [Samples].[Brand] ([Id]),
    constraint [FK_Samples_Shirt_HeroImage] foreign key ([HeroImageId]) references [Framework].[ImageFile] ([Id]),
    constraint [FK_Samples_Shirt_GuidePdf] foreign key ([GuidePdfId]) references [Framework].[PdfFile] ([Id]),
);